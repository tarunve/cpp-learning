/*
 *  ->  C++ was designed with the main intention of adding object-oriented
 *      features of C language.
 *  ->  As the size of the program inrceases, readability, maintainability and
 *      bug-free nature of programs decreases.
 *  ->  This was the major problem with language C which relied upon functions
 *      or procedures (hence the name procedural programming language).
 *  ->  As a result, the possibility of not addressing the problem in an 
 *      efficientt manner was high.
 *  ->  Also, as data was almost neglected, data security was easily compromised.
 *  ->  Using classes solves this problem by modelling program as a real world scenario.
 * 
 *  OOPS:
 *  ====
 *  ->  Works on the concept of classes and objects.
 *  ->  Class is a template to create objects.
 *  ->  Treats data as a critical element.
 *  ->  Decomposes the problems in objects and build data and functions arround the objects.
 *  1.  Classes :-  Basic template for creating objects.
 *  2.  Objects :-  Basic run time entities.
 *  3.  Data Abstraction and Encapsulation :-   Wrapping data and functions into single unit.
 *  4.  Inheritance :-  Properties of one clas can be inherited into others.
 *  5.  Polymorphism :- ability to take more than one forms.
 *  6.  Dynamic Binding :-  code which will execute is not known until the program runs.
 *  7.  Message Passing :-  Object.message(Information) call format.  
 * 
 *  Benefits:
 *  ========
 *  ->  Better code reusability using objects and inheritance.
 *  ->  Principle of data hidiing helps build secure systems.
 *  ->  Mutiple objects can co-exist without any interference.
 *  ->  Software Complexity can be easily managed.
 */